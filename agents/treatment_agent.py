def get_treatment(disease_name: str) -> dict:
    treatment = TREATMENT_DB.get(disease_name, GENERIC_TREATMENT)

    print(f"💊 Treatment found for: {disease_name}")
    print(f"   Severity: {treatment['severity']}")
    print(f"   Medicine: {treatment['chemical_treatment']['medicine']}")

    return treatment


def format_treatment_for_voice(treatment: dict, disease_name: str) -> str:
    severity_map = {
        "high": "बहुत गंभीर",
        "medium": "मध्यम",
        "none": "कोई समस्या नहीं",
        "unknown": "अज्ञात"
    }

    severity_hindi = severity_map.get(treatment['severity'], "मध्यम")

    if treatment['severity'] == 'none':
        opening = f"खुशखबरी! {treatment['disease_hindi']}।"
    else:
        opening = f"आपकी फसल में {treatment['disease_hindi']} पाई गई है।"

    voice_text = f"""
{opening}
गंभीरता: {severity_hindi}।

जैविक उपाय: {treatment['organic_treatment'][0]}

रासायनिक उपाय: {treatment['chemical_treatment']['medicine']}, 
{treatment['chemical_treatment']['dosage']} पानी में मिलाकर छिड़काव करें।

सावधानी: {treatment['avoid'][0]}

अपने नजदीकी कृषि केंद्र से संपर्क करें।
"""
    return voice_text.strip()